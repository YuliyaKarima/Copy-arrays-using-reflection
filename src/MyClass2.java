public class MyClass2 {
	// class attribute
	private int i_a;

	/**
	 * setter
	 * 
	 * @param a_a
	 *            int value
	 */
	public void setA(int a_a) {
		i_a = a_a;
	}

	/**
	 * getter
	 * 
	 * @return attribute a
	 */
	public int getA() {
		return i_a;
	}

	/**
	 * Constructor sets value to attribute a
	 * 
	 * @param a_a
	 */
	public MyClass2(int a_a) {
		i_a = a_a;
	}
}
